package com.example.grupoc.cubikate;

public class AltaMaestro {
    private String RUT_maestro, nombres, apellidos, direccion, telefono, email;

    public String getRUT_maestro() {
        return RUT_maestro;
    }

    public void setRUT_maestro(String RUT_maestro) {
        this.RUT_maestro = RUT_maestro;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public String getApellidos() {
        return apellidos;
    }

    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
